  pi.registerCommand("ultraplan", {
    description:
      "Decompose a complex task into milestones — the agent dynamically selects reviewers",
    handler: async (args, ctx) => {
      const confirmed = await ctx.ui.confirm(
        "Start Agentic Milestone Planning (Ultraplan)",
        "The agent will:\n1. Compose a Problem Brief\n2. Decide which reviewer perspectives are needed\n3. Dispatch reviewers in parallel\n4. Synthesize a milestone DAG\n\nProceed?"
      );
      if (!confirmed) return;

      currentPhase = "ultraplanning";
      updateState(STATE_FILE, { phase: "ultraplanning" }).catch(() => {});
      ctx.ui.setStatus("JINHO", "Agentic milestone workflow in progress...");

      const topic = args?.trim() || "";
      const prompt = topic
        ? `Decompose the following IDS task into milestones: "${topic}"\n\nFollow the agentic-milestone-planning skill rules.\n1. Compose a Problem Brief — identify which IDS service domains are affected and what the Phase 0/1/2 boundary is.\n2. Dispatch all 5 reviewer agents in parallel: reviewer-feasibility, reviewer-architecture, reviewer-risk, reviewer-dependency, reviewer-user-value.\n   - reviewer-architecture: validate 4-layer DDD compliance per milestone.\n   - reviewer-risk: flag OpenStack integration risk and ML model assumption risk.\n3. Synthesize findings into a milestone DAG aligned with IDS phases.`
        : `Decompose the current IDS task into milestones.\n\nFollow the agentic-milestone-planning skill rules.\n1. Compose a Problem Brief — identify which IDS service domains (planning/prediction/deployment/alerting) are involved and what Phase (0/1/2) the work targets.\n2. Dispatch all 5 reviewer agents in parallel: reviewer-feasibility, reviewer-architecture, reviewer-risk, reviewer-dependency, reviewer-user-value.\n3. Synthesize their findings into a milestone DAG aligned with IDS development phases.`;

      pi.sendUserMessage(prompt);
    },
  });

  pi.registerCommand("ask", {
    description: "Manual smoke test for the ask_user_question tool",
    handler: async (args, ctx) => {
      const topic = args?.trim() || "Ask me one focused question using the ask_user_question tool.";
      const confirmed = await ctx.ui.confirm(
        "Run /ask",
        "The agent will send a manual prompt that requires one ask_user_question tool call.\n\nProceed?"
      );
      if (!confirmed) return;

      currentPhase = "idle";
      ctx.ui.setStatus("JINHO", "Manual ask_user_question test in progress...");

      pi.sendUserMessage(
        `Manual tool test: use the ask_user_question tool exactly once, then stop. User context: "${topic}"`
      );
    },
  });

  const setupHandler = async (_args: string, ctx: any) => {
    const settingsPath = join(homedir(), ".pi", "agent", "settings.json");

    let current: Record<string, unknown> = {};
    try {
      const raw = await readFile(settingsPath, "utf-8");
      current = JSON.parse(raw);
    } catch {
    }

    if (current.quietStartup === true) {
      ctx.ui.notify("Settings already configured — quietStartup is true.", "info");
      return;
    }

    const ok = await ctx.ui.confirm(
      "Setup: Configure Recommended Settings",
      [
        "This will add \"quietStartup\": true to your settings.json:",
        `  ${settingsPath}`,
        "",
        "This hides the default Skills/Extensions/Themes listing at startup.",
        "The JINHO banner takes over instead.",
        "",
        "Proceed?",
      ].join("\n"),
    );
    if (!ok) return;

    const updated = { ...current, quietStartup: true };
    await mkdir(dirname(settingsPath), { recursive: true });
    await writeFile(settingsPath, JSON.stringify(updated, null, 2) + "\n");

    ctx.ui.notify("Settings updated — quietStartup is now true. Restart pi to see the effect.", "info");
  };

