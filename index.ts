async function findJinhoMd(startDir: string): Promise<string | null> {
  let dir = startDir;
  while (true) {
    const candidate = join(dir, "JINHO.md");
    try {
      await readFile(candidate, "utf-8");
      return candidate;
    } catch { /* not here */ }
    const parent = dirname(dir);
    if (parent === dir) return null;
    dir = parent;
  }
}

